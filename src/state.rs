/// Truncate a command's stderr to the first 300 chars for error messages.
pub fn stderr_snippet(out: &std::process::Output) -> String {
    snippet(&String::from_utf8_lossy(&out.stderr))
}

/// Truncate to the first 300 chars for error messages.
fn snippet(s: &str) -> String {
    s.chars().take(300).collect()
}

/// Extract the percentage from a yt-dlp progress line (PROGRESS_PREFIX + " 23.4%" etc.).
/// Returns None for non-progress lines or indeterminate values ("N/A" / non-finite),
/// since f64::parse accepts "nan"/"inf" which become null in JSON.
fn parse_progress_percent(line: &str) -> Option<f64> {
    let rest = line.strip_prefix(PROGRESS_PREFIX)?;
    rest.trim()
        .strip_suffix('%')?
        .trim()
        .parse()
        .ok()
        .filter(|p: &f64| p.is_finite())
}

/// Run yt-dlp and return stdout on success within the timeout.
/// On failure or timeout, return an error message (including stderr snippet).
pub async fn run_yt_dlp(args: &[&str], timeout: time::Duration) -> Result<Vec<u8>, String> {
    let cmd = Command::new("yt-dlp").args(args).output();
    let out = time::timeout(timeout, cmd)
        .await
        .map_err(|_| "yt-dlp timed out".to_string())?
        .map_err(|e| format!("Failed to run yt-dlp: {e}"))?;

    if !out.status.success() {
        return Err(format!("yt-dlp failed: {}", stderr_snippet(&out)));
    }
    Ok(out.stdout)
}

/// Fetch metadata JSON via yt-dlp (no download).
async fn fetch_metadata(url: &str) -> Result<Value, String> {
    let stdout = run_yt_dlp(
        &["--dump-json", "--no-download", url],
        time::Duration::from_secs(METADATA_TIMEOUT_SECS),
    )
    .await
    .map_err(|e| format!("Failed to fetch metadata: {e}"))?;

    serde_json::from_slice(&stdout).map_err(|e| format!("Failed to parse metadata: {e}"))
}

/// Return the next (dir=1) or previous (dir=-1) track relative to current_id.
/// Wraps around at the ends. If current_id is not found (deleted etc.),
/// returns the first (dir=1) or last (dir=-1) track.
fn neighbor_track(tracks: &[AudioTrack], current_id: &str, dir: isize) -> Option<AudioTrack> {
    if tracks.is_empty() {
        return None;
    }
    let len = tracks.len() as isize;
    let idx = match tracks.iter().position(|t| t.id == current_id) {
        Some(i) => (i as isize + dir).rem_euclid(len),
        None if dir >= 0 => 0,
        None => len - 1,
    };
    tracks.get(idx as usize).cloned()
}

/// Pick a random track excluding current_id (returns that track if only one exists).
fn random_track_from(mut tracks: Vec<AudioTrack>, current_id: &str) -> Option<AudioTrack> {
    if tracks.len() > 1 {
        tracks.retain(|t| t.id != current_id);
    }
    if tracks.is_empty() {
        return None;
    }
    // Nanoseconds of the current time are random enough for track selection variety
    let nanos = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .subsec_nanos() as usize;
    Some(tracks.swap_remove(nanos % tracks.len()))
}

/// Generate a playlist ID ("pl" + time-derived value; unique enough for creation frequency).
fn new_playlist_id() -> String {
    let d = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default();
    format!("pl{:x}{:05x}", d.as_millis(), d.subsec_nanos() & 0xfffff)
}

/// Classified input URL type (video / playlist / unknown).
pub enum UrlKind {
    Video,
    Playlist(String),
    Unknown,
}

/// Classify a URL as video, playlist, or unknown. URLs containing both a video
/// ID and a playlist ID (e.g. a watch URL during playlist playback) are treated
/// as video for backward compatibility.
pub fn classify_url(url: &str) -> UrlKind {
    if extract_video_id(url).is_some() {
        UrlKind::Video
    } else if let Some(list_id) = extract_playlist_id(url) {
        UrlKind::Playlist(list_id)
    } else {
        UrlKind::Unknown
    }
}

/// Extract a YouTube playlist ID from the list= parameter. Validation is
/// minimal — whether it can be expanded is left to yt-dlp, so that auth-gated
/// lists (WL etc.) return error messages to the user.
fn extract_playlist_id(url: &str) -> Option<String> {
    use std::sync::OnceLock;

    static RE: OnceLock<Option<Regex>> = OnceLock::new();
    let re = RE
        .get_or_init(|| Regex::new(r"youtube\.com/\S*[?&]list=([a-zA-Z0-9_-]{2,})").ok())
        .as_ref()?;
    Some(re.captures(url)?.get(1)?.as_str().to_string())
}

/// Check whether a string matches the YouTube video ID format.
fn is_video_id(s: &str) -> bool {
    use std::sync::OnceLock;

    static RE: OnceLock<Option<Regex>> = OnceLock::new();
    RE.get_or_init(|| Regex::new(&format!("^{VIDEO_ID_PATTERN}$")).ok())
        .as_ref()
        .is_some_and(|re| re.is_match(s))
}

fn extract_video_id(url: &str) -> Option<String> {
    use std::sync::OnceLock;

    static PATTERNS: OnceLock<Vec<Regex>> = OnceLock::new();
    let patterns = PATTERNS.get_or_init(|| {
        [
            format!(r"(?:youtube\.com/watch\?.*v=|youtu\.be/)({VIDEO_ID_PATTERN})"),
            format!(r"youtube\.com/embed/({VIDEO_ID_PATTERN})"),
            format!(r"youtube\.com/shorts/({VIDEO_ID_PATTERN})"),
            format!(r"youtube\.com/live/({VIDEO_ID_PATTERN})"),
        ]
        .iter()
        .filter_map(|p| Regex::new(p).ok())
        .collect()
    });

    for re in patterns {
        if let Some(caps) = re.captures(url) {
            return caps.get(1).map(|m| m.as_str().to_string());
        }
    }
    None
}

/// List {video_id}.m4a files in audio_cache as (video_id, path) pairs.
fn cached_video_ids(cache_dir: &Path) -> Vec<(String, PathBuf)> {
    let Ok(entries) = std::fs::read_dir(cache_dir) else {
        return Vec::new();
    };
    entries
        .filter_map(|e| e.ok())
        .filter_map(|e| {
            let path = e.path();
            if path.extension().is_none_or(|ext| ext != AUDIO_EXT) {
                return None;
            }
            let stem = path.file_stem()?.to_str()?;
            if !is_video_id(stem) {
                return None;
            }
            Some((stem.to_string(), path))
        })
        .collect()
}

/// Return a file's mtime as UNIX seconds (falls back to current time).
fn file_mtime_f64(path: &Path) -> f64 {
    std::fs::metadata(path)
        .and_then(|m| m.modified())
        .ok()
        .and_then(|t| t.duration_since(UNIX_EPOCH).ok())
        .map(|d| d.as_secs_f64())
        .unwrap_or_else(now_f64)
}

pub fn now_f64() -> f64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_secs_f64()
}

